    private void showThongTin() {
        if (!tfMaBacSi.getText().equals("")) {
            String sql = "Select * from \"BacSi\" where lower(\"IDBS\")=?";
            try {
                stmt = con.prepareStatement(sql);
                stmt.setString(1, tfMaBacSi.getText().toLowerCase().trim());
                rs = stmt.executeQuery();
                if (rs.next()) {
                    tfTen.setText(rs.getString(2).toString());
                    tfNgaySinh.setText(rs.getDate(3).toString());
                    tfTuoi.setText(new Integer(rs.getInt(4)).toString());
                    if (rs.getInt(5) == 1) {
                        tfGioiTinh.setText("Nam");
                    } else {
                        tfGioiTinh.setText("Nữ");
                    }
                    tfDiaChi.setText(rs.getString(6).trim());
                    tfChuyenMon.setText(rs.getString(7));
                    tfThamNien.setText(new Integer(rs.getShort(8)).toString());
                    tfHeSoLuong.setText(new Double(rs.getDouble(9)).toString());
                    tfMaKhoa.setText(rs.getString(10));
                }
                else{
                    JOptionPane.showMessageDialog(null, "Không tồn tại mã bác sĩ. Hãy kiểm tra lại");
                }

            } catch (Exception e) {
                
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn phải nhập mã bác sĩ!");
        }
    }

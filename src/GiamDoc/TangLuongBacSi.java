    private void btHoanTatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHoanTatActionPerformed
        if (tfHeSoLuongMoi.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Bạn chưa nhập hệ số lương mới.");
        } else {
            int click = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn thay đổi hệ số lương cho bác sĩ " + tfTen.getText().trim() + " không?");
            if (click == JOptionPane.YES_OPTION) {
                String sql1 = "Update \"BacSi\" set \"HSLuong\" = ? where lower(\"IDBS\") = ?";//tat nhac di
                try {
                    stmt = con.prepareStatement(sql1);
                    stmt.setDouble(1, Double.parseDouble(tfHeSoLuongMoi.getText().toString()));
                    //System.out.println(Double.parseDouble(tfHeSoLuongMoi.getText().toString()));
                    stmt.setString(2, tfMaBacSi.getText().trim().toLowerCase());
                    System.out.println(stmt);
                    stmt.executeUpdate();
                    System.out.println("1");
                    JOptionPane.showMessageDialog(null, "Thay hệ số lương mới thành công!");
                } catch (Exception e) {
                    System.out.println(e.toString());
                }
            }
        }
    }//GEN-LAST:event_btHoanTatActionPerformed

